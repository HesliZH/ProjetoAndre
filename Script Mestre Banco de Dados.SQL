CREATE TABLE SETORES(
    CODIGO INT SERIAL PRIMARY KEY,
    DESCRICAO VARCHAR(50) NOT NULL,
);

CREATE TABLE ALUNO(
    CODIGO INT SERIAL PRIMARY KEY,
    NOME_COMPLETO VARCHAR(50) NOT NULL,
    RA VARCHAR(25) NOT NULL,
    SENHA VARCHAR(100) NOT NULL
);

CREATE TABLE PROFESSORES(
    CODIGO INT SERIAL PRIMARY KEY,
    NOME_COMPLETO VARCHAR(80) NOT NULL,
    CPF VARCHAR(20) NOT NULL,
    TELEFONE VARCHAR(20) NOT NULL,
    ATIVO CHAR(1) DEFAULT 'S' NOT NULL
)

CREATE TABLE STATUS(
    CODIGO INT SERIAL PRIMARY KEY,
    DESCRICAO VARCHAR(60) NOT NULL,
    ORDENACAO INT NOT NULL
);

CREATE TABLE RECLAMACAO(
    ID_OPERACAO INT SERIAL NOT NULL,
    ALUNO INT REFERENCES ALUNO(CODIGO) NOT NULL,
    SETOR INT REFERENCES SETORES(CODIGO) NOT NULL,
    STATUSOPERACAO INT REFERENCES STATUS(CODIGO) NOT NULL,
    RESPOSTA VARCHAR(500) NOT NULL,
    PROFESSOR INT REFERENCES PROFESSORES(CODIGO)
)